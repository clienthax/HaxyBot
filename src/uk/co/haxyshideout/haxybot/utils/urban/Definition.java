public class Definition {
	private String defid;
	private String word;
	private String author;
	private String permalink;
	private String definition;
	private String example;
	private int thumbs_up;
	private int thumbs_down;
	private String current_vote;
	public String getDefId() { return defid; }
	public String getWord() { return word; }
	public String getAuthor() { return author; }
	public String getPermalink() { return permalink; }
	public String getDefinition() { return definition; }
	public String getExample() { return example; }
	public int getThumbsUp() { return thumbs_up; }
	public int getThumbsDown() { return thumbs_down; }
	public String getCurrentVote() { return current_vote; }
}
